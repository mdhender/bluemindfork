package net.bluemind.eas.backend;

public class MoveSourceAndDestination {

	private HierarchyNode source;
	private HierarchyNode destination;

	public static MoveSourceAndDestination create(HierarchyNode source, HierarchyNode destination) {
		MoveSourceAndDestination ret = new MoveSourceAndDestination();
		ret.source = source;
		ret.destination = destination;
		return ret;
	}

	public HierarchyNode getSource() {
		return source;
	}

	public HierarchyNode getDestination() {
		return destination;
	}

	@Override
	public int hashCode() {
		final int prime = 31;
		int result = 1;
		result = prime * result + ((destination == null) ? 0 : destination.hashCode());
		result = prime * result + ((source == null) ? 0 : source.hashCode());
		return result;
	}

	@Override
	public boolean equals(Object obj) {
		if (this == obj)
			return true;
		if (obj == null)
			return false;
		if (getClass() != obj.getClass())
			return false;
		MoveSourceAndDestination other = (MoveSourceAndDestination) obj;
		if (destination == null) {
			if (other.destination != null)
				return false;
		} else if (!destination.equals(other.destination))
			return false;
		if (source == null) {
			if (other.source != null)
				return false;
		} else if (!source.equals(other.source))
			return false;
		return true;
	}

}
